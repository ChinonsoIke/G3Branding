    $(document).ready(function(){
       var blocks = []
       var changedBlocks =[];
        $('inline-content-block').each(function(index,elem){
            elem = $(this); elem.html("")
            if(elem.attr("contenttype")=="text"){
              var edtor = $('<div>');
              edtor.attr('name','block')
              edtor.attr('class','editor'+index)
              
              edtor.html(elem.attr('content'))
              elem.append(edtor)
              //if(elem:contains('<image')){
                  InlineEditor.create( document.querySelector('div.editor'+index) , {toolbar: [ 'heading', '|', 'bold', 'italic', 'link','imageTextAlternative' ]
  
                                },{image: {
                            toolbar: [ 'imageTextAlternative' ]
                        }})
  
            }else if(elem.attr("contenttype") == "image"){
              var imgEdtor = $('<div>');
                imgEdtor.attr("class","upload-img")
                elem.html(elem.attr('content'))
                elem.after(imgEdtor)
                if(elem.attr("source_value") =="home_about_desc_image"){
                  $(".upload-img").uploader({
                    size:{
                        width:"150px",
                        height:"150px"
                    }
                    
                  });
                }else{
                  $(".upload-img").uploader()
                }
                
            }
        })
  
        $("div[class^='editor']").each(function(){
          $(this).blur(function(){
            var inlineEditor =  $(this).parent("inline-content-block");
            console.log(inlineEditor);
            inlineEditor.attr("content", $(this).html())
            changedBlocks.push({
                rawText         : inlineEditor.attr("rawText"),
                validationRules : inlineEditor.attr("validationRules"),
                table           : inlineEditor.attr("table"),
                source_key      : inlineEditor.attr("source_key"),
                source_value    : inlineEditor.attr("source_value"),
                target_key      : inlineEditor.attr("target_key"),
                target_value    : inlineEditor.attr("content"),
                content : inlineEditor.attr("content")
            })
            
          }) 
        })
  
        $("#applyChanges").on("click",function(){
          //$http.post('/laravel-inline-editor', {blocks: changedBlocks})_token: $('meta[name="csrf-token"]').attr('content')
          $.post('/laravel-inline-editor', {blocks: changedBlocks}).then(
                function(response){//response.status
                    if( response == "ok" ) {
                        swal({
                        position: 'top-end',
                        type: 'success',
                        title: 'Your work has been saved',
                        showConfirmButton: false,
                        timer: 1500
                      });
                        setTimeout(function(){window.location.reload()},6000);
                    }else{
                        swal("Record saving failed", response.msg, "info")
                    }
                },
                function(xhr){
                    console.log(xhr);
                    var response = JSON.parse(xhr.responseText);
                    if (response.errors) {
                        var errorString = '<ul>';
                        $.each(response.errors, function( key, value) {
                            errorString += '<li>' + value + '</li>';
                        });
                        errorString += '</ul>';
                        swal("Unexpected Error!", errorString, "error")
                    }
                    if( xhr.responseText.message === 'Invalid argument supplied for foreach()') {
                      swal({
                        type: 'error',
                        title: 'The given data was invalid',
                        text: response.body.errors.target_value
                      });
                    }
                    else {
                      swal({
                        type: 'error',
                        title: 'Oops...',
                        text: 'Something went wrong!'
                      });
                    }
                }
            )
        })
    })
  </script>
  